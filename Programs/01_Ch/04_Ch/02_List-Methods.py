list = ["Apple", "orange", 5, 56.5, "harshu", "Boys"]
print(list)

# Append Method : =
list.append("Girls") # Add in the End of List
print(list)

# Sort 
l1 = [1,76,89,122,0,2,76,43]
l1.sort()
print(l1)


# Reverse 
l1.reverse()
print(l1)

# Insert 
l1.insert(4,33)  # Index : value which we want to add
print(l1)


# Pop (remove index value)

l2 = [1,76,89,122,0,2,76,43]
l2.pop(5)
print(l2)


# Remove (Specific value remove)
l3 = [1,6,9,12,0,2,76,43]
l3.remove(6)
print(l3)